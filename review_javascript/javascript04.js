// 문제점: 이렇게 쓰면 코드가 너무 지저분해지고 읽기 어려워집니다. 그래서 보통은 이렇게 잘 안 씁니다.

// 2. 함수 내부에서 b()를 실행하는 경우 (더 권장되는 방식)
// 자바스크립트에서 함수를 인수로 넘기는 가장 큰 이유는 **"나중에 원할 때 실행해라"**라고 맡기는 것입니다. 이것을 콜백(Callback) 함수라고 부릅니다.

// JavaScript
// function funcB(a, b) {
