// 만약 funcB(1, funcC()) 처럼 호출하고 싶다면, 이미 정의된 함수를 사용해야 합니다. 하지만 질문자님은 호출하는 자리에 **익명 함수(이름 없는 함수)**를 직접 작성하고 계시죠?

// 익명 함수를 인수로 넣으면서 동시에 실행까지 하려면 문법이 조금 복잡해집니다.

// JavaScript
