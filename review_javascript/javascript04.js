function funcA() {
  return 1;
}

function funcB(a, b) {
  let c = a + b();
  console.log(c);
}

funcB(1, function funcC() {
  return 1;
});
// 왜 funcB 인스로 b() 하면 안되는 건데? 왜 내용에 b()를 해야돼

// 궁금증이 명확해지셨군요! 결론부터 말씀드리면, funcB를 호출할 때 인수로 b()를 쓰는 것과 함수 내부에서 b()를 하는 것은 아예 "누가 실행하느냐"의 차이입니다.

// 상황별로 어떤 일이 벌어지는지 아주 쉽게 풀어드릴게요.

